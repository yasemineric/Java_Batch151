package b151practices.practice.backend;

public class Kahve {
    private String cesit;
    private boolean sutlu;
    private int seker;
    private String boy;
    private int fiyat;

    public String getCesit() {
        return cesit;
    }

    public void setCesit(String cesit) {
        this.cesit = cesit;
    }

    public boolean isSutlu() {
        return sutlu;
    }

    public void setSutlu(boolean sutlu) {
        this.sutlu = sutlu;
    }

    public int getSeker() {
        return seker;
    }

    public void setSeker(int seker) {
        this.seker = seker;
    }

    public String getBoy() {
        return boy;
    }

    public void setBoy(String boy) {
        this.boy = boy;
    }

    public int getFiyat() {
        return fiyat;
    }

    public void setFiyat(int fiyat) {
        this.fiyat = fiyat;
    }
}
